[[nodiscard]] std::size_t checked_add(std::size_t a, std::size_t b, std::string_view context) {
    if (b > std::numeric_limits<std::size_t>::max() - a) {
        throw std::runtime_error(std::format("{} size overflow.", context));
    }
    return a + b;
}

[[nodiscard]] std::optional<std::size_t> searchSig(std::span<const Byte> v, std::span<const Byte> sig, std::size_t limit = 0) {
    auto search_span = (limit == 0 || limit > v.size())
        ? v
        : v.first(limit);

    if (sig.empty() || sig.size() > search_span.size()) {
        return std::nullopt;
    }

    const Byte* const base = search_span.data();
    const std::size_t last_start = search_span.size() - sig.size();
    std::size_t pos = 0;
    while (pos <= last_start) {
        const void* match = std::memchr(base + pos, sig.front(), last_start - pos + 1);
        if (!match) {
            return std::nullopt;
        }

        pos = static_cast<const Byte*>(match) - v.data();
        if (sig.size() == 1 || std::memcmp(v.data() + pos + 1, sig.data() + 1, sig.size() - 1) == 0) {
            return pos;
        }
        ++pos;
    }
    return std::nullopt;
}

void updateValue(vBytes& vec, std::size_t index, std::size_t value, std::size_t length = 2) {
    if (length > vec.size() || index > vec.size() - length) {
        throw std::out_of_range("updateValue: Index out of bounds.");
    }

    switch (length) {
        case 2: {
            if (value > std::numeric_limits<std::uint16_t>::max()) {
                throw std::out_of_range("updateValue: Value overflow for 2-byte field.");
            }
            const auto v = static_cast<std::uint16_t>(value);
            vec[index] = static_cast<Byte>((v >> 8) & 0xFFu);
            vec[index + 1] = static_cast<Byte>(v & 0xFFu);
            break;
        }
        case 4: {
            if (value > std::numeric_limits<std::uint32_t>::max()) {
                throw std::out_of_range("updateValue: Value overflow for 4-byte field.");
            }
            const auto v = static_cast<std::uint32_t>(value);
            vec[index] = static_cast<Byte>((v >> 24) & 0xFFu);
            vec[index + 1] = static_cast<Byte>((v >> 16) & 0xFFu);
            vec[index + 2] = static_cast<Byte>((v >> 8) & 0xFFu);
            vec[index + 3] = static_cast<Byte>(v & 0xFFu);
            break;
        }
        default:
            throw std::invalid_argument("updateValue: Unsupported field length.");
    }
}

[[nodiscard]] std::optional<std::size_t> searchText(std::span<const Byte> haystack, std::string_view needle, std::size_t limit = 0) {
    if (needle.empty()) {
        return std::nullopt;
    }
    auto needle_bytes = std::span<const Byte>(reinterpret_cast<const Byte*>(needle.data()), needle.size());
    return searchSig(haystack, needle_bytes, limit);
}

[[nodiscard]] std::optional<std::size_t> find_tag_start(std::span<const Byte> data, std::size_t from) {
    if (data.empty() || from >= data.size()) {
        return std::nullopt;
    }

    for (std::size_t i = from;; --i) {
        if (data[i] == static_cast<Byte>('<')) {
            return i;
        }
        if (i == 0) {
            break;
        }
    }
    return std::nullopt;
}

[[nodiscard]] std::optional<std::size_t> find_tag_end(std::span<const Byte> data, std::size_t from) {
    if (from >= data.size()) {
        return std::nullopt;
    }

    for (std::size_t i = from; i < data.size(); ++i) {
        if (data[i] == static_cast<Byte>('>')) {
            return i;
        }
    }
    return std::nullopt;
}

void strip_external_link_tags(vBytes& vec) {
    constexpr auto LINK_PREFIX = std::to_array<Byte>({'<', 'l', 'i', 'n', 'k'});

    auto erase_links_for_domain = [&](std::string_view domain) {
        while (true) {
            auto domain_pos_opt = searchText(vec, domain);
            if (!domain_pos_opt) {
                break;
            }

            const std::size_t domain_pos = *domain_pos_opt;
            auto start_opt = find_tag_start(vec, domain_pos);
            auto end_opt = find_tag_end(vec, domain_pos);

            if (!start_opt || !end_opt) {
                throw std::runtime_error("Template Error: Failed to parse external link tag.");
            }

            const std::size_t tag_start = *start_opt;
            const std::size_t tag_end = *end_opt;
            if (tag_start + LINK_PREFIX.size() > vec.size()) {
                throw std::runtime_error("Template Error: Invalid external link tag bounds.");
            }

            const auto prefix = std::span<const Byte>(vec.data() + tag_start, LINK_PREFIX.size());
            if (!std::ranges::equal(prefix, LINK_PREFIX)) {
                throw std::runtime_error("Template Error: External resource domain not found in <link> tag.");
            }

            vec.erase(vec.begin() + static_cast<std::ptrdiff_t>(tag_start),
                      vec.begin() + static_cast<std::ptrdiff_t>(tag_end + 1));
        }
    };

    erase_links_for_domain("fonts.googleapis.com");
    erase_links_for_domain("fonts.gstatic.com");
}

struct TemplateReplacement {
    std::size_t pos = 0;
    std::span<const Byte> marker;
    std::string_view replacement;
};

[[nodiscard]] TemplateReplacement find_replacement(
    const vBytes& vec,
    std::span<const Byte> marker,
    std::string_view replacement,
    std::string_view marker_name) {
    auto pos_opt = searchSig(vec, marker);
    if (!pos_opt) {
        throw std::runtime_error(std::format("Template Error: Missing {} marker.", marker_name));
    }
    return TemplateReplacement{
        .pos = *pos_opt,
        .marker = marker,
        .replacement = replacement,
    };
}

void replace_template_markers(
    vBytes& vec,
    std::span<const Byte> url_marker,
    std::string_view url_replacement,
    std::span<const Byte> prompt_marker,
    std::string_view prompt_replacement) {
    std::array replacements{
        find_replacement(vec, url_marker, url_replacement, "URL"),
        find_replacement(vec, prompt_marker, prompt_replacement, "PROMPT"),
    };

    std::ranges::sort(replacements, {}, &TemplateReplacement::pos);
    if (replacements[0].pos + replacements[0].marker.size() > replacements[1].pos) {
        throw std::runtime_error("Template Error: Replacement markers overlap.");
    }

    std::size_t output_size = vec.size();
    for (const auto& replacement : replacements) {
        output_size -= replacement.marker.size();
        output_size = checked_add(output_size, replacement.replacement.size(), "Template");
    }

    vBytes out;
    out.reserve(output_size);

    std::size_t cursor = 0;
    for (const auto& replacement : replacements) {
        out.insert(out.end(), vec.begin() + static_cast<std::ptrdiff_t>(cursor),
                   vec.begin() + static_cast<std::ptrdiff_t>(replacement.pos));
        out.insert(out.end(), replacement.replacement.begin(), replacement.replacement.end());
        cursor = replacement.pos + replacement.marker.size();
    }
    out.insert(out.end(), vec.begin() + static_cast<std::ptrdiff_t>(cursor), vec.end());
    vec.swap(out);
}
