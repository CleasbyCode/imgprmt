typedef unsigned char Byte;

void 
	// Start imgprmt and prepare files and vectors.
	Open_Files(char* []),
	
	// Certain characters may break the imgprmt web page, such as the apostrophe character.
	// We need to search the prompt string & replace it (and other problem chars) with a html entity code
	// to prevent it from breaking the imgprmt web page.
	Problem_Chars(std::wstring& str),

	// Writes updated size values (e.g. iCC Profile length) into relevant vector index locations. Overwrites previous size values.
	Value_Updater(std::vector<Byte>&, size_t, const size_t&, int),

	// Program information.
	Display_Info();

int main(int argc, char** argv) {

	if (argc == 2 && std::string(argv[1]) == "--info") {
		Display_Info();
	}
	else if (argc > 1 && argc < 3) {
		Open_Files(argv);
	}
	else {
		std::cout << "\nUsage: imgprmt <jpg-image>\n\t\bimgprmt --info\n\n";
	}
	return 0;
}

